    void intialize(String cat) {
        for(Item i : items)
            if(i.getText().equalsIgnoreCase(cat))
                items_list1.setSelectedValue(i, true);
        jPanel6.revalidate();
        jPanel6.removeAll();
        jPanel6.repaint();
    // topPanel1.setText(cat);
        //jPanel6.setBackground(Color.yellow);
        Sell s = this;
        try {

            for (Table t : TablesList.getTables()) {
                if (t.getCat().equalsIgnoreCase(cat)) {
                    TableListButton b = new TableListButton(t);

                    jPanel6.add(b);
                    b.addActionListener(new ActionListener() {

                        @Override
                        public void actionPerformed(ActionEvent ae) {
                            new Table_popup(t, s).setVisible(true);
                            s.setEnabled(false);
                        }
                    });
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Sell.class.getName()).log(Level.SEVERE, null, ex);
        }
        jPanel5.requestFocus();
    }

